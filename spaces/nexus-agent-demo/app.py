def run_pipeline(prompt: str) -> dict:
    start = time.time()
    trace_id = uuid.uuid4().hex[:12]
    steps = []

    # Step 1: Input scan
    input_scan = scan_input(prompt)
    steps.append({
        "step": "1. Immune Input Scan",
        "verdict": input_scan.verdict.upper(),
        "score": input_scan.score,
        "triggers": input_scan.triggers,
    })

    if input_scan.verdict == "block":
        elapsed = round((time.time() - start) * 1000, 1)
        return {
            "trace_id": trace_id,
            "status": "BLOCKED",
            "status_emoji": "🛑",
            "response": None,
            "error": "Input blocked by immune scanner",
            "steps": steps,
            "latency_ms": elapsed,
        }

    working_prompt = prompt
    if input_scan.verdict == "flag":
        hardened, removed = harden_prompt(prompt)
        if hardened.strip():
            working_prompt = hardened
            steps[-1]["hardened"] = True
            steps[-1]["removed_fragments"] = removed
        else:
            elapsed = round((time.time() - start) * 1000, 1)
            return {
                "trace_id": trace_id,
                "status": "BLOCKED",
                "status_emoji": "🛑",
                "response": None,
                "error": "Prompt entirely composed of flagged content",
                "steps": steps,
                "latency_ms": elapsed,
            }

    # Step 2: A-S-FLC (skipped in demo)
    steps.append({"step": "2. A-S-FLC Decision Analysis", "verdict": "SKIPPED", "note": "Short prompt — analysis not needed"})

    # Step 3: LLM Generation
    response = mock_generate(working_prompt)
    steps.append({"step": "3. LLM Generation", "verdict": "DONE", "model": "mock", "tokens": len(response.split())})

    # Step 4: Critic evaluation
    critic = critic_evaluate(working_prompt, response)
    steps.append({
        "step": "4. Arbiter Critic Evaluation",
        "verdict": critic["verdict"].upper(),
        "scores": critic["scores"],
    })

    if critic["verdict"] == "halt":
        halted_by = [n for n, s in critic["scores"].items() if s["verdict"] == "fail"]
        elapsed = round((time.time() - start) * 1000, 1)
        return {
            "trace_id": trace_id,
            "status": "HALTED",
            "status_emoji": "⚠️",
            "response": None,
            "error": f"Halted by critic: {', '.join(halted_by)}",
            "steps": steps,
            "latency_ms": elapsed,
        }

    # Step 5: Governance (auto-allow for demo)
    steps.append({"step": "5. Covernor Governance", "verdict": "ALLOW", "policy": "demo-allow-all", "risk": "low"})

    # Step 6: Output scan
    output_scan = scan_output(response)
    steps.append({
        "step": "6. Immune Output Scan",
        "verdict": output_scan.verdict.upper(),
        "score": output_scan.score,
        "triggers": output_scan.triggers,
    })

    if output_scan.verdict == "block":
        elapsed = round((time.time() - start) * 1000, 1)
        return {
            "trace_id": trace_id,
            "status": "BLOCKED",
            "status_emoji": "🛑",
            "response": None,
            "error": "Output blocked — leaked sensitive data",
            "steps": steps,
            "latency_ms": elapsed,
        }

    # Step 7: Success
    prompt_hash = hashlib.sha256(prompt.encode()).hexdigest()[:16]
    response_hash = hashlib.sha256(response.encode()).hexdigest()[:16]
    steps.append({
        "step": "7. Hash-Chained Trace",
        "verdict": "PERSISTED",
        "prompt_hash": prompt_hash,
        "response_hash": response_hash,
    })

    elapsed = round((time.time() - start) * 1000, 1)
    return {
        "trace_id": trace_id,
        "status": "COMPLETED",
        "status_emoji": "✅",
        "response": response,
        "error": None,
        "steps": steps,
        "latency_ms": elapsed,
    }


