def format_steps(steps: list[dict]) -> str:
    lines = []
    for s in steps:
        verdict = s.get("verdict", "?")
        if verdict in ("PASS", "DONE", "ALLOW", "SKIPPED", "PERSISTED"):
            icon = "✅"
        elif verdict == "FLAG":
            icon = "🟡"
        elif verdict in ("BLOCK", "HALT"):
            icon = "🛑"
        else:
            icon = "⚪"

        line = f"{icon} **{s['step']}** — {verdict}"

        if s.get("score") is not None:
            line += f" (score: {s['score']:.2f})"
        if s.get("triggers"):
            triggers_str = ", ".join(s["triggers"][:3])
            if len(s["triggers"]) > 3:
                triggers_str += f" (+{len(s['triggers']) - 3} more)"
            line += f"\n   Triggers: `{triggers_str}`"
        if s.get("hardened"):
            line += f"\n   Hardened: removed {len(s.get('removed_fragments', []))} injection fragment(s)"
        if s.get("scores"):
            for node, score_data in s["scores"].items():
                sv = score_data["verdict"].upper()
                node_icon = "✅" if sv == "PASS" else ("🟡" if sv == "WARN" else "🛑")
                line += f"\n   {node_icon} {node}: {score_data['score']:.2f} ({sv})"
        if s.get("model"):
            line += f" — model: {s['model']}, tokens: {s.get('tokens', '?')}"
        if s.get("policy"):
            line += f" — policy: {s['policy']}, risk: {s.get('risk', '?')}"
        if s.get("prompt_hash"):
            line += f"\n   Hashes: prompt=`{s['prompt_hash']}…` response=`{s.get('response_hash', '?')}…`"
        if s.get("note"):
            line += f" — {s['note']}"

        lines.append(line)
    return "\n\n".join(lines)


def process(prompt: str) -> tuple[str, str, str]:
    if not prompt.strip():
        return "Enter a prompt above.", "", ""

    result = run_pipeline(prompt)

    status_line = f"## {result['status_emoji']} {result['status']}"
    status_line += f" &nbsp;|&nbsp; Trace: `{result['trace_id']}` &nbsp;|&nbsp; Latency: {result['latency_ms']}ms"

    if result.get("error"):
        status_line += f"\n\n**Reason:** {result['error']}"

    pipeline_detail = format_steps(result["steps"])

    response_text = result.get("response") or "*No response — blocked before or during generation.*"

    return status_line, pipeline_detail, response_text


